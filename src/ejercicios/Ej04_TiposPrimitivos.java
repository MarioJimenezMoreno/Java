package ejercicios;

public class Ej04_TiposPrimitivos {

	public static void main(String[] args) {
		// Lógicos
		boolean typeBool = true;
		System.out.println("Boolean = " + typeBool);
		// Caracter
		char textChar = 'a';
		System.out.println("Char = " + textChar);
		// Numéricos enteros
		byte numByte = 127;
		System.out.println("Byte = " + numByte);
		short numShort = 32767;
		System.out.println("Short = " + numShort);
		int numInt = 2147483647;
		System.out.println("Int = " + numInt);
		long numLong = 9223372036854775807l;
		System.out.println("Long = " + numLong);
		// Numéricos reales
		float numFloat = 0.123456789f;
		System.out.println("Float = " + numFloat);
		double numDouble = 0.123456789123456789;
		System.out.println("Double = " + numDouble);
	}

}
