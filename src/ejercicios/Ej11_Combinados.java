package ejercicios;

public class Ej11_Combinados {

	public static void main(String[] args) {
		int num = 25;
		System.out.println(num);
		num += 5;
		System.out.println(num);
		num -= 15;
		System.out.println(num);
		num *= 2;
		System.out.println(num);
		num /= 2;
		System.out.println(num);
		num %= 3;
		System.out.println(num);
	}

}
