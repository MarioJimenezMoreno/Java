package ejercicios;

public class Ej09_Operadores {

	public static void main(String[] args) {
		int resta = 10 - 5;
		int suma = 55 + 45;
		int negativo = -3;
		int multiplicacion = 5*5;
		int divisionInt = 25/7;
		float divisionFloat = 25f/7f;
		int resto = 25%4;
		System.out.println("Resta: "+resta);
		System.out.println("Suma: "+suma);
		System.out.println("Numero negativo: "+negativo);
		System.out.println("Multiplicacion: "+multiplicacion);
		System.out.println("Division entera: "+divisionInt);
		System.out.println("Division real: "+divisionFloat);
		System.out.println("Resto: "+resto);
	}

}
