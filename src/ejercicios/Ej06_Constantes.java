package ejercicios;

public class Ej06_Constantes {

	public static void main(String[] args) {
		// Constantes
		final boolean TYPE_BOOL = true;
		System.out.println("Boolean = " + TYPE_BOOL);
		final char TEXT_CHAR = 'a';
		System.out.println("Char = " + TEXT_CHAR);
		final byte NUM_BYTE = 127;
		System.out.println("Byte = " + NUM_BYTE);
		final short NUM_SHORT = 32767;
		System.out.println("Short = " + NUM_SHORT);
		final int NUM_INT = 2147483647;
		System.out.println("Int = " + NUM_INT);
		final long NUM_LONG = 9223372036854775807l;
		System.out.println("Long = " + NUM_LONG);
		final float NUM_FLOAT = 0.123456789f;
		System.out.println("Float = " + NUM_FLOAT);
		final double NUM_DOUBLE = 0.123456789123456789;
		System.out.println("Double = " + NUM_DOUBLE);
	}
}
